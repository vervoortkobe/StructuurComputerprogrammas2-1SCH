// Print een 3X5 matrix
void print_matrix(double result[][5]) {
  for (int row = 0; row < 3; row++) {
    printf("[");
    for (int column = 0; column < 5; column++) {
      printf("%g", result[row][column]);
      if (column != 4) { // Niet de laatste kolom (5 - 1)
        printf(",");
      }
    }
    printf("]\n");
  }
}



