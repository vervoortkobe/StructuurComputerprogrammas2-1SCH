void print_primes(int n) {
  int i;
  for (i = 2; i < n; i++) {
    int prime = 1;
    int j;
    for (j = 2; j < i; j++) {
      if (i % j == 0) {
        prime = 0;
      }
    }
    if (prime) {
      printf("%i\n", i);
    }
  }
}

